#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    // literals
    Identifier,
    Integer,
    Float,
    String,
    FString,

    // keywords
    Fn,
    Let,
    Const,
    If,
    Else,
    Elif,
    While,
    For,
    In,
    Return,
    True,
    False,
    Not,
    And,
    Or,
    Pass,
    Break,
    Continue,
    Import,
    From,
    Struct,
    Impl,
    Try,
    As,
    Assert,
    Mut,
    Enum,
    Match,
    Async,
    Await,
    Case,

    // operators
    Plus,
    Minus,
    Star,
    DoubleStar,
    Slash,
    Percent,
    Equal,
    DoubleEqual,
    NotEqual,
    Less,
    LessEqual,
    Greater,
    GreaterEqual,
    PlusEqual,
    MinusEqual,
    StarEqual,
    SlashEqual,
    PercentEqual,
    DoubleStarEqual,
    DoubleSlash,
    DoubleSlashEqual,
    Shl,
    Shr,
    ShlEqual,
    ShrEqual,
    Ampersand,
    Pipe,

    // symbols
    LParen,
    RParen,
    LBracket,
    RBracket,
    LBrace,
    RBrace,
    Colon,
    Comma,
    Dot,
    Arrow,
    Semicolon,
    At,
    Underscore,
    DoubleColon,
    Question,
    Hash,

    // layout
    Newline,
    Indent,
    Dedent,

    // end
    Eof,
}

#[derive(Debug, Clone)]
pub struct Token {
    pub kind: TokenKind,
    pub value: String,
    pub line: usize,
    pub col: usize,
    pub span: (usize, usize), // char offsets [start, end)
    pub file_id: usize,
}
