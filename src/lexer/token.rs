#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    Identifier,
    Integer,
    Float,
    String,
    FString,

    Fn,
    Let,
    Const,
    If,
    Else,
    Elif,
    While,
    For,
    In,
    Return,
    True,
    False,
    Not,
    And,
    Or,
    Pass,
    Break,
    Continue,
    Import,
    From,
    Struct,
    Impl,
    Trait,
    Try,
    As,
    Assert,
    Mut,
    Enum,
    Match,
    Async,
    Await,
    Case,
    Unsafe,
    Null,

    Plus,
    Minus,
    Star,
    DoubleStar,
    Slash,
    Percent,
    Equal,
    DoubleEqual,
    NotEqual,
    Less,
    LessEqual,
    Greater,
    GreaterEqual,
    PlusEqual,
    MinusEqual,
    StarEqual,
    SlashEqual,
    PercentEqual,
    DoubleStarEqual,
    Shl,
    Shr,
    ShlEqual,
    ShrEqual,
    Ampersand,
    Pipe,

    LParen,
    RParen,
    LBracket,
    RBracket,
    LBrace,
    RBrace,
    Colon,
    Comma,
    Dot,
    Arrow,
    Semicolon,
    At,
    Underscore,
    DoubleColon,
    Question,
    Hash,

    Newline,
    Indent,
    Dedent,

    Eof,
}

#[derive(Debug, Clone)]
pub struct Token {
    pub kind: TokenKind,
    pub value: String,
    pub line: usize,
    pub col: usize,
    pub span: (usize, usize),
    pub file_id: usize,
}
